		<h1 align="center">EL HADJI OUSSEYNOU DIOUF : Biographie</h1>
	<p para1>Natif de Dakar,il passe son enfance à Saint-Louis, l'ancienne capitale du Sénégal. El-Hadji commence le football dans les quartiers au poste de gardien de but, poste qu'il quitte quand son équipe est menée pour devenir attaquant, avec Jules Bocandé comme modèle. À 13 ans, il se fait remarquer à Dakar lors de la semaine de la jeunesse1. Il a eu une présélection dans l'équipe nationale des moins de 17 ans gérée à l'époque par Joseph Koto et Mayacine Mar2. Un an plus tard, l'adolescent quitte le Sénégal. Il débarque au RC Lens mais l'expérience échoue après douze jours de stage. N'étant pas jugé au niveau, il intègre ensuite le FC Sochaux. Soutenu par Francis Gillot et François Blaquart, le jeune joueur s'habitue à la vie française et aux exigences du centre de formation. Le Sénégalais montre vite l'étendue de ses qualités balle au pied aux côtés de futurs professionnels comme Meriem, Frau, Pedretti et Daf. Diouf débute avec les moins de 15 ans, grandit avec les moins de 17 ans et mûrit avec l'équipe réserve en CFA. Le 11 novembre 1998, à peine dix-sept ans, il débute en D1 contre le SC Bastia (2-1) puis participe à quinze rencontres durant la saison.


