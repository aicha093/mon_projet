D’abord gardien, c’est pour imiter son compatriote Jules Bocandé que le jeune El-Hadji change de poste. Bien lui en aura pris. Né au Sénégal, El-Hadji Diouf arrive en France à son adolescence, et intègre le centre de formation du FC Sochaux. Quelques années plus tard, il fait ses débuts en D1 et rejoint après une seule saison dans l’élite le Stade Rennais. En Bretagne, après de bons débuts, Diouf peine à convaincre. Auteur de quatre buts, il se distingue surtout par ses écarts extra-sportifs. En 2000, le club breton le prête au RC Lens. C’est dans le Nord que le natif de Dakar explose. Joueur-clé des Sang et Or pendant deux saisons, ses prestations permettent à son club de finir vice-champion de France en 2002. Egalement finaliste de la Coupe d'Afrique des Nations la même année, El-Hadji Diouf enchaîne l’été suivant sur un Mondial éblouissant. Sous son impulsion, les Lions de la Teranga égalent le Cameroun en devenant la seconde nation africaine à se hisser jusqu’en quart de finale du rendez-vous planétaire, et ce après avoir notamment terrassé la France (championne du monde en titre) durant la phase de poules!.</p>


